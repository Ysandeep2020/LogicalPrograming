package com.sk.Pattern;

public class TringleTest {

	public static void main(String[] args) {

		// R and D on Triangle
		for (int i = 1; i <= 5; i++) {

			for (int j = 1; j <= 2 * i - 1; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}

		// R and D on Triangle print odd Start

		for (int i = 1; i <= 5; i++) {

			for (int j = 1; j <= 2 * i - 1; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}

	}
}
